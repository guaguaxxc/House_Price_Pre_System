def standardize_province_name(province):
    """
    移除省份名称后缀，转为简化版（匹配PROVINCE_TO_REGION的key）
    示例："辽宁省" → "辽宁"、"广西壮族自治区" → "广西"、"北京市" → "北京"
    """
    # 需要移除的后缀列表（覆盖所有情况）
    suffixes = ["省", "自治区", "市", "特别行政区"]
    # 先统一转为字符串，避免非字符串类型报错
    province = str(province).strip()
    # 遍历移除后缀
    for suffix in suffixes:
        province = province.replace(suffix, "")
    # 特殊处理："内蒙古自治区" → "内蒙古"（避免只删"自治区"后剩"内蒙古"，已兼容）
    return province
